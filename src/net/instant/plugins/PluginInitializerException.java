package net.instant.plugins;

public class PluginInitializerException extends PluginException {

    public PluginInitializerException() {
        super();
    }
    public PluginInitializerException(String message) {
        super(message);
    }
    public PluginInitializerException(Throwable cause) {
        super(cause);
    }
    public PluginInitializerException(String message, Throwable cause) {
        super(message, cause);
    }


}
